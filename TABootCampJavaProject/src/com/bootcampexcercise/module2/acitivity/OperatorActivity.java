package com.bootcampexcercise.module2.acitivity;

/**
 * Activity 2
 */
public class OperatorActivity {

    public static void main(String args[]) {
        // Initialize two integer values
        int i = 89, j = 77;
        int sum = i + j;
        System.out.println("Sum with variable: " + sum);
        System.out.println("Sum without variable: " + (i + j));

        // With negative integers example
        int a = 89, b = -77;

        System.out.println("Sum without variable: " + (a + b));

    }
}
